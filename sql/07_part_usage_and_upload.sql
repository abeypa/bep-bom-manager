-- ============================================================
-- TABLE: part_usage_logs
-- ============================================================
CREATE TABLE IF NOT EXISTS part_usage_logs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  project_name TEXT NOT NULL,
  site_name TEXT,
  use_date_time TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  part_number TEXT NOT NULL,
  part_table_name TEXT NOT NULL,
  quantity INT NOT NULL DEFAULT 0,
  created_date TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

-- Create indexes for performance
CREATE INDEX IF NOT EXISTS idx_part_usage_project ON part_usage_logs(project_name);
CREATE INDEX IF NOT EXISTS idx_part_usage_part_number ON part_usage_logs(part_number);
CREATE INDEX IF NOT EXISTS idx_part_usage_date ON part_usage_logs(use_date_time);
CREATE INDEX IF NOT EXISTS idx_part_usage_table ON part_usage_logs(part_table_name);

-- Add comment to table
COMMENT ON TABLE part_usage_logs IS 'Audit trail for part usage across projects';

-- Add comments to columns
COMMENT ON COLUMN part_usage_logs.id IS 'Unique identifier for usage log entry';
COMMENT ON COLUMN part_usage_logs.project_name IS 'Name of project where part was used';
COMMENT ON COLUMN part_usage_logs.site_name IS 'Site/location where part was used';
COMMENT ON COLUMN part_usage_logs.use_date_time IS 'Date and time when part was used';
COMMENT ON COLUMN part_usage_logs.part_number IS 'Part number that was used';
COMMENT ON COLUMN part_usage_logs.part_table_name IS 'Source table for the part (e.g., mechanical_manufacture)';
COMMENT ON COLUMN part_usage_logs.quantity IS 'Quantity used';
COMMENT ON COLUMN part_usage_logs.created_date IS 'Timestamp when log entry was created';

-- ============================================================
