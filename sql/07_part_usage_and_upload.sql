-- ============================================================
-- Function to log part usage automatically
-- ============================================================

-- Function to create part usage log when parts are used in projects
CREATE OR REPLACE FUNCTION log_part_usage_from_project()
RETURNS TRIGGER AS $$
DECLARE
    project_record RECORD;
    part_record RECORD;
    part_table_name TEXT;
    part_number_val TEXT;
BEGIN
    -- Get project information
    SELECT p.project_name INTO project_record
    FROM projects p
    JOIN project_sections ps ON p.id = ps.project_id
    WHERE ps.id = NEW.project_section_id;
    
    -- Determine which parts table and get part number
    CASE NEW.part_type
        WHEN 'mechanical_manufacture' THEN
            SELECT part_number INTO part_record FROM mechanical_manufacture WHERE id = NEW.part_id;
            part_table_name := 'mechanical_manufacture';
        WHEN 'mechanical_bought_out' THEN
            SELECT part_number INTO part_record FROM mechanical_bought_out WHERE id = NEW.part_id;
            part_table_name := 'mechanical_bought_out';
        WHEN 'electrical_manufacture' THEN
            SELECT part_number INTO part_record FROM electrical_manufacture WHERE id = NEW.part_id;
            part_table_name := 'electrical_manufacture';
        WHEN 'electrical_bought_out' THEN
            SELECT part_number INTO part_record FROM electrical_bought_out WHERE id = NEW.part_id;
            part_table_name := 'electrical_bought_out';
        WHEN 'pneumatic_bought_out' THEN
            SELECT part_number INTO part_record FROM pneumatic_bought_out WHERE id = NEW.part_id;
            part_table_name := 'pneumatic_bought_out';
    END CASE;
    
    -- Create usage log entry
    INSERT INTO part_usage_logs (
        project_name,
        part_number,
        part_table_name,
        quantity,
        use_date_time
    ) VALUES (
        project_record.project_name,
        part_record.part_number,
        part_table_name,
        NEW.quantity,
        NOW()
    );
    
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Create trigger to log part usage when parts are added to projects
CREATE TRIGGER log_part_usage_on_project_part_added
    AFTER INSERT ON project_parts
    FOR EACH ROW
    EXECUTE FUNCTION log_part_usage_from_project();

-- Create trigger to log part usage when project part quantity changes
CREATE TRIGGER log_part_usage_on_project_part_updated
    AFTER UPDATE ON project_parts
    FOR EACH ROW
    WHEN (OLD.quantity != NEW.quantity)
    EXECUTE FUNCTION log_part_usage_from_project();

