COMMENT ON COLUMN json_excel_file_uploaded.user_name IS 'User who performed the upload';
COMMENT ON COLUMN json_excel_file_uploaded.date_time IS 'Date and time of upload';
COMMENT ON COLUMN json_excel_file_uploaded.json_content_type IS 'Type of content uploaded (e.g., JSON, Excel)';
