-- Create a trigger to automatically update updated_date for purchase_orders
CREATE OR REPLACE FUNCTION update_updated_date_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_date = NOW();
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER update_purchase_orders_updated_date 
    BEFORE UPDATE ON purchase_orders 
    FOR EACH ROW 
    EXECUTE FUNCTION update_updated_date_column();

-- Create a trigger to calculate total_amount for purchase_order_items
CREATE OR REPLACE FUNCTION calculate_poi_total_amount()
RETURNS TRIGGER AS $$
BEGIN
    NEW.total_amount = NEW.unit_price * NEW.quantity * (1 - COALESCE(NEW.discount_percent, 0) / 100);
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER calculate_poi_total_amount_on_insert
    BEFORE INSERT ON purchase_order_items
    FOR EACH ROW
    EXECUTE FUNCTION calculate_poi_total_amount();

CREATE TRIGGER calculate_poi_total_amount_on_update
    BEFORE UPDATE ON purchase_order_items
    FOR EACH ROW
    EXECUTE FUNCTION calculate_poi_total_amount();

-- Create a function to update PO summary when items change
CREATE OR REPLACE FUNCTION update_po_summary()
RETURNS TRIGGER AS $$
BEGIN
    -- Update grand_total, total_items, and total_quantity in purchase_orders
    UPDATE purchase_orders po
    SET 
        grand_total = (
            SELECT COALESCE(SUM(total_amount), 0)
            FROM purchase_order_items poi
            WHERE poi.purchase_order_id = po.id
        ),
        total_items = (
            SELECT COUNT(*)
            FROM purchase_order_items poi
            WHERE poi.purchase_order_id = po.id
        ),
        total_quantity = (
            SELECT COALESCE(SUM(quantity), 0)
            FROM purchase_order_items poi
            WHERE poi.purchase_order_id = po.id
        ),
        updated_date = NOW()
    WHERE po.id = (
        CASE 
            WHEN TG_OP = 'DELETE' THEN OLD.purchase_order_id
            ELSE NEW.purchase_order_id
        END
    );
    
    RETURN NULL;
END;
$$ language 'plpgsql';

-- Create triggers to update PO summary
CREATE TRIGGER update_po_summary_on_insert
    AFTER INSERT ON purchase_order_items
    FOR EACH ROW
    EXECUTE FUNCTION update_po_summary();

CREATE TRIGGER update_po_summary_on_update
    AFTER UPDATE ON purchase_order_items
    FOR EACH ROW
    EXECUTE FUNCTION update_po_summary();

CREATE TRIGGER update_po_summary_on_delete
    AFTER DELETE ON purchase_order_items
    FOR EACH ROW
    EXECUTE FUNCTION update_po_summary();

